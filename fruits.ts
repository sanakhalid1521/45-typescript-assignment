
interface Fruit {
    name: string;
    color: string;
    taste: string;
}

// Create fruit objects
const apple: Fruit = {
    name: "Apple",
    color: "Red",
    taste: "Sweet"
};

const banana: Fruit = {
    name: "Banana",
    color: "Yellow",
    taste: "Sweet"
};

const orange: Fruit = {
    name: "Orange",
    color: "Orange",
    taste: "Citrusy"
};

// Print out information about each fruit object
console.log("Apple:", apple);
console.log("Banana:", banana);
console.log("Orange:", orange);
